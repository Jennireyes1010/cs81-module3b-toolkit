function isEven(n) {
    return n % 2 === 0;
}
console.log(isEven(7));

function square(n) {
    return n * n;
}
console.log(square(9));

function cube(n) {
    return n * n * n;
}
console.log(cube(6));

function isOdd(n) {
    return n % 2 !== 0;
}
console.log(isOdd(4));